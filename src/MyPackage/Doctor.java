    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        table_doctor.setModel(new DefaultTableModel(null, new String[]{"DID","Doctor Name","Doctor Gender","Doctor DoB","Doctor Contact","Doctor Department","Doctor Address"}));
        table_doctor.getColumnModel().getColumn(5).setPreferredWidth(200);
        table_doctor.getColumnModel().getColumn(6).setPreferredWidth(200);
        DefaultTableModel model = (DefaultTableModel)table_doctor.getModel();
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(URL, User, Password);
            PreparedStatement pst=null;

            if (jComboBox1.getSelectedIndex()==0) {
                String query = "Select * from doctor where dname =?";
                pst = con.prepareStatement(query);
                pst.setString(1, name.getText());
            }
            if(jComboBox1.getSelectedIndex()==1){
                String query = "Select * from doctor where did=? and dname =?";
                pst = con.prepareStatement(query);
                pst.setInt(1, new Integer(id.getText()));
                pst.setString(2, name.getText());
            }

            @SuppressWarnings("null")
            ResultSet rs = pst.executeQuery();
            while(rs.next()){
                int did = rs.getInt(1);
                String dname = rs.getString(2);
                String dcontact = rs.getString(3);
                String ddept = rs.getString(4);
                String dgender = rs.getString(7);
                String ddob = rs.getString(5);
                String daddress = rs.getString(6);

                model.addRow(new Object[]{did,dname,dgender,ddob,dcontact,ddept,daddress});

            }
     
        }
        catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_searchActionPerformed

