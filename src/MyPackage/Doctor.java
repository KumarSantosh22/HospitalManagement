    private void insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertActionPerformed
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(URL, User, Password);
            String query = "Insert into Doctor(DName,DDoB,DGender,DContact,DAddress,DDepartment) values(?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = con.prepareStatement(query);

            pst.setString(1, name.getText());
            pst.setString(2, ((JTextField)dobChooser.getDateEditor().getUiComponent()).getText());
            pst.setString(3, gender.getSelectedItem().toString());
            pst.setString(4, contact.getText());
            pst.setString(5, address.getText());
            pst.setString(6, dept.getSelectedItem().toString());

            pst.execute();
            JOptionPane.showMessageDialog(null, "Data Inserted !");
            pst.close();
            con.close();
        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Error "+e);
        }
    }//GEN-LAST:event_insertActionPerformed

