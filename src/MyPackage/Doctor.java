    private void idKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idKeyReleased
        if (option.getSelectedIndex()==2) {
            int p_id = Integer.parseInt(id.getText());
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection(URL, User, Password);
                PreparedStatement pst = con.prepareStatement("Select * from doctor where did = ?");
                pst.setInt(1, p_id);

                ResultSet rs = pst.executeQuery();
                if(rs.next()){
                   
                    String dname = rs.getString(2);
                    String dcontact = rs.getString(3);
                    String ddept = rs.getString(4);
                    String dgender = rs.getString(7);
                    String ddob = rs.getString(5);
                    String daddress = rs.getString(6);

                    name.setText(dname);contact.setText(dcontact);address.setText(daddress);
                    ((JTextField)dobChooser.getDateEditor().getUiComponent()).setText(ddob);gender.setSelectedItem(dgender);dept.setSelectedItem(ddept);

                }
                else{
                    name.setText("");contact.setText("");address.setText("");
                    ((JTextField)dobChooser.getDateEditor().getUiComponent()).setText("");gender.setSelectedItem("");dept.setSelectedItem("");
                }

            } catch (ClassNotFoundException | SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_idKeyReleased

